class Matrix2 {
  int linha,coluna;

  Matrix2(int linha,int coluna)
  {
    this.linha=linha;
    this.coluna=coluna;
  }

  void setLinha(int linha)
  {
    this.linha=linha;
  }

  void setColuna(int coluna)
  {
    this.linha=coluna;
  }

  int getLinha()
  {
    return this.linha;
  }

  int getColuna()
  {
    return this.coluna;
  }

}